    private void eliminarBOTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarBOTONActionPerformed
		//Confirmamos borrado
		int confirmaBorrado = JOptionPane.showOptionDialog(
				this,
				"Confirmar borrado",
				"Selector de opciones",
				JOptionPane.YES_NO_OPTION,
				JOptionPane.QUESTION_MESSAGE,
				null, // null para el icono por defecto.
				new Object[]{"Borrar", "Cancelar"},
				"Cancelar");
		if (confirmaBorrado == JOptionPane.OK_OPTION) {
			//Si no quedan alumnos saldrá aviso
			if (this.miAula.getListaAlumnos().size() == 1) {
				this.miAula.getListaAlumnos().remove(this.alumnoSelected);
				this.miAula.getListaAlumnos().add(new Alumno());
				JOptionPane.showMessageDialog(
						this, "Se han borrado todos los alumnos. Creamos uno vacio");
			} else {
				//Si no es el último, borramos alumno y cambia posición al anterior
				this.miAula.getListaAlumnos().remove(this.alumnoSelected);
				//salvo si es alumno 0, que se mantiene la posición
				if (this.alumnoSelected != 0) {
					this.alumnoSelected--;
				}
			}

			actualizaFormulario();

		} else {
			//No se borra nada y avisamos
			JOptionPane.showMessageDialog(
					this, "Cancelamos borrado");
		}


    }//GEN-LAST:event_eliminarBOTONActionPerformed

