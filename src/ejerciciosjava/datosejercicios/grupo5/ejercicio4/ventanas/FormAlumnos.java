	private void actualizaAlumno() {
		Alumno datosAl = this.miAula.getListaAlumnos().get(this.alumnoSelected);
		String rutaFoto = "datos\\" + this.miAula.getPropietario()
				+ "\\" + this.miAula.getAula() + "\\fotos\\";

		codAlumTXTFIELD.setText(String.valueOf(datosAl.getCodAlumno()));
		dniTXTFIELD.setText(datosAl.getDni());
		nombreTXTFIELD.setText(datosAl.getNombre());
		apellidosTXTFIELD.setText(datosAl.getApellidos());

		fechaNacDATECHOOSER.setDate(datosAl.getFechaNac().getTime());
		fechaIngDATECHOOSER.setDate(datosAl.getFechaInscr().getTime());

		this.dirSelected = datosAl.getPosDirPorDefecto();
		this.contactSelected = datosAl.getPosContactPorDefecto();

		if (datosAl.getSexo() == Sexo.HOMBRE) {
			hombreRADIO.setSelected(true);
		} else {
			mujerRADIO.setSelected(true);
		}
		if (datosAl.getEstadoCivil() == EstadoCivil.CASADO) {
			casadoRADIO.setSelected(true);
		} else {
			solteroRADIO.setSelected(true);
		}

		ImageIcon foto = new ImageIcon(rutaFoto + datosAl.getFoto());

		ImageIcon fotoAdaptada = new ImageIcon(foto.getImage().getScaledInstance(
				fotoLABEL.getWidth(),
				fotoLABEL.getHeight(),
				Image.SCALE_DEFAULT)
		);
		fotoLABEL.setIcon(fotoAdaptada);

		estudioCOMBOBOX.setSelectedItem(datosAl.getEstudioSuperior());

		lengJavaCHECK.setSelected(false);
		lengCCHECK.setSelected(false);
		lengCSharpCHECK.setSelected(false);
		lengJSCHECK.setSelected(false);
		for (Lenguaje l : datosAl.getEstudiaLenguaje()) {

			switch (l) {
				case JAVA:
					lengJavaCHECK.setSelected(true);
					break;
				case C:
					lengCCHECK.setSelected(true);
					break;
				case CSHARP:
					lengCSharpCHECK.setSelected(true);
					break;
				case JAVASCRIPT:
					lengJSCHECK.setSelected(true);
					break;
			}
		}

	}

