	private void guardaCamposForm() {

		//Alumno seleccionado actualmente
		Alumno datosAl = this.miAula.getListaAlumnos().get(this.alumnoSelected);

		//Guardamos datos de los campos en ese alumno
		//Datos alumno en general
		datosAl.setDni(dniTXTFIELD.getText());
		datosAl.setNombre(nombreTXTFIELD.getText());
		datosAl.setApellidos(apellidosTXTFIELD.getText());
		datosAl.setFechaNac(fechaNacDATECHOOSER.getCalendar());
		datosAl.setFechaInscr(fechaIngDATECHOOSER.getCalendar());

		if (hombreRADIO.isSelected()) {
			datosAl.setSexo(Sexo.HOMBRE);
		} else {
			datosAl.setSexo(Sexo.MUJER);
		}

		if (solteroRADIO.isSelected()) {
			datosAl.setEstadoCivil(EstadoCivil.SOLTERO);
		} else {
			datosAl.setEstadoCivil(EstadoCivil.CASADO);
		}
		datosAl.setEstudioSuperior((TipoEstudio) estudioCOMBOBOX.getSelectedItem());

		//lista lenguajes
		ArrayList<Lenguaje> newlenguaje = new ArrayList<>();
		JCheckBox[] listaLeng = new JCheckBox[]{lengCCHECK, lengCSharpCHECK, lengJavaCHECK, lengJSCHECK};

		for (JCheckBox lcheck : listaLeng) {
			if (lcheck.isSelected()) {
				switch (lcheck.getText()) {
					case "C":
						newlenguaje.add(Lenguaje.C);
						break;
					case "C#":
						newlenguaje.add(Lenguaje.CSHARP);
						break;
					case "Java":
						newlenguaje.add(Lenguaje.JAVA);
						break;
					case "JavaScript":
						newlenguaje.add(Lenguaje.JAVASCRIPT);
						break;
				}
			}
		}

		datosAl.setEstudiaLenguaje(newlenguaje);

		//Datos direccion
		Direccion datosDir = datosAl.getDirecciones().get(this.dirSelected);
		datosDir.setTipoCalle((TiposCalle) tipoCalleDirCOMBOBOX.getSelectedItem());
		datosDir.setNumEdif(numCalleDirTXTFIELD.getText());
		datosDir.setPiso(pisoCalleDirTXTFIELD.getText());
		datosDir.setPuerta(puertaDirTXTFIELD.getText());
		datosDir.setCodigoPostal(codPostalDirTXTFIELD.getText());

		//Datos contacto
		Contacto datosContacto = datosAl.getContactos().get(this.contactSelected);
		datosContacto.setTipo((TipoContacto) tipoContactoCOMBOBOX.getSelectedItem());
		datosContacto.setDesc(descContactoTXTFIELD.getText());

	}

	private void actualizaFormulario() {
		actualizaAlumno();
		actualizaDirecciones();
		actualizaContactos();
	}
